//  *******************CHARACTER FUNCTIONS*******************
#include<iostream>
#include<Ctype.h>
using namespace std;
int main(){
	cout<< "ISALNUM IS : "<<isalnum('l')<<endl;
	cout<< "ISALPHA IS : "<<isalpha('W')<<endl;
	cout<< "ISDIGIT IS : "<<isdigit('8')<<endl;
	cout<< "ISSPACE IS : "<<isspace('  ')<<endl;
	cout<< "ISLOWER IS : "<<islower('w')<<endl;
	cout<< "ISUPPER IS : "<<isupper('A')<<endl;
	cout<< "ISXDIGIT IS: "<<isxdigit('F')<<endl;
	cout<< "TOLOWER IS : "<<tolower('H')<<endl;
	cout<< "TOUPPER IS : "<<toupper('d')<<endl;
}
